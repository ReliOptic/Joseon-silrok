import { readFileSync, readdirSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataDir = join(__dirname, '../src/data');
const manifestPath = join(dataDir, 'story-manifest.ts');

const KING_ORDER = [
  'taejo', 'jeongjong', 'taejong', 'sejong', 'munjong', 'danjong', 'sejo',
  'yejong', 'seongjong', 'yeonsangun', 'jungjong', 'injong', 'myeongjong',
  'seonjo', 'gwanghaegun', 'injo', 'hyojong', 'hyeonjong', 'sukjong',
  'gyeongjong', 'yeongjo', 'jeongjo', 'sunjo', 'heonjong', 'cheoljong',
  'gojong', 'sunjong',
];

function findMatchingBracket(text, startIdx, openCh, closeCh) {
  let depth = 0;
  for (let i = startIdx; i < text.length; i++) {
    const ch = text[i];
    if (ch === openCh) depth++;
    else if (ch === closeCh) {
      depth--;
      if (depth === 0) return i;
    }
  }
  return -1;
}

function extractEventsBlock(content) {
  const m = content.match(/events\s*:\s*\[/);
  if (!m) return null;
  const arrStart = m.index + m[0].length - 1;
  const arrEnd = findMatchingBracket(content, arrStart, '[', ']');
  if (arrEnd < 0) return null;
  return content.slice(arrStart + 1, arrEnd);
}

function splitTopLevelObjects(eventsBody) {
  const objects = [];
  let i = 0;
  while (i < eventsBody.length) {
    while (i < eventsBody.length && eventsBody[i] !== '{') i++;
    if (i >= eventsBody.length) break;
    const end = findMatchingBracket(eventsBody, i, '{', '}');
    if (end < 0) break;
    objects.push(eventsBody.slice(i + 1, end));
    i = end + 1;
  }
  return objects;
}

function extractStoryTitle(eventBody) {
  const storyIdx = eventBody.search(/storyEntry\s*:/);
  if (storyIdx < 0) return null;
  const sub = eventBody.slice(storyIdx);
  const titleMatch = sub.match(/title\s*:\s*['"`]([^'"`]+)['"`]/);
  return titleMatch ? titleMatch[1] : null;
}

const entries = [];

for (const kingId of KING_ORDER) {
  const file = join(dataDir, `${kingId}.ts`);
  const content = readFileSync(file, 'utf-8');
  const eventsBody = extractEventsBlock(content);
  if (!eventsBody) {
    console.warn(`[warn] ${kingId}: events block not found`);
    continue;
  }
  const events = splitTopLevelObjects(eventsBody);
  events.forEach((evBody, eventIndex) => {
    const yearMatch = evBody.match(/year\s*:\s*(\d+)/);
    if (!yearMatch) return;
    const hasStory = /storyEntry\s*:/.test(evBody);
    if (!hasStory) return;
    const titleMatch = evBody.match(/title\s*:\s*['"`]([^'"`]+)['"`]/);
    entries.push({
      kingId,
      eventIndex,
      year: parseInt(yearMatch[1], 10),
      title: titleMatch ? titleMatch[1] : '',
      storyTitle: extractStoryTitle(evBody) ?? '',
    });
  });
}

const banner = '// AUTO-GENERATED by scripts/build-story-manifest.mjs — do not edit manually.\n';
const typeDecl = `export interface StoryManifestEntry {
  readonly kingId: string;
  readonly eventIndex: number;
  readonly year: number;
  readonly title: string;
  readonly storyTitle: string;
}\n\n`;

const arrLiteral = entries.map(e =>
  `  { kingId: '${e.kingId}', eventIndex: ${e.eventIndex}, year: ${e.year}, title: ${JSON.stringify(e.title)}, storyTitle: ${JSON.stringify(e.storyTitle)} },`
).join('\n');

const out = `${banner}\n${typeDecl}export const STORY_MANIFEST: readonly StoryManifestEntry[] = [\n${arrLiteral}\n];\n`;
writeFileSync(manifestPath, out, 'utf-8');

const byKing = entries.reduce((acc, e) => {
  acc[e.kingId] = (acc[e.kingId] ?? 0) + 1;
  return acc;
}, {});
console.log(`Wrote ${entries.length} story entries across ${Object.keys(byKing).length} kings → ${manifestPath}`);
console.log('Per-king counts:', byKing);
